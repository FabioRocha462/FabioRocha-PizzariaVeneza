    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Pizzas pi=new Pizzas();
        Pedido pe=new Pedido();
        pi.setNome(pesquisacliente.getText());
        pi.setSabor(saborpizza.getText());
        
        try{
        double preco=Double.parseDouble(precopizza.getText());
        pi.setPreco(preco);
        }catch(NumberFormatException e){
            e.getMessage();
            precopizza.setText(precopizza.getText().replaceAll(",","."));
            double preco=Double.parseDouble(precopizza.getText());
            pi.setPreco(preco);
        }
        
        pe.fazerpedido(pi);
        NotaJanela nj =new NotaJanela();
        
        nj.setVisible(true);
        nomecliente.setText("");
        pesquisacliente.setText("");
        saborpizza.setText("");
        precopizza.setText("");
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ArrayList<Pessoa> lista  = (ArrayList<Pessoa>)FilePessoa.recuperarPessoa("arquivo1.txt");
        String chave=nomecliente.getText();
        for(Pessoa p:lista){
            if(p instanceof Cliente){
            if(chave.equalsIgnoreCase(p.getNome())){
                pesquisacliente.setText(p.imprimir());

            }
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

