    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ArrayList<Pessoa> lista  = (ArrayList<Pessoa>)FilePessoa.recuperarPessoa("arquivo1.txt");
        String nome=nomefornecedor.getText();
        for(Pessoa p: lista){
            if(p instanceof Fornecedor){
                if(nome.equalsIgnoreCase(p.getNome())){
                    pesquisafornecedor.setText(p.imprimir());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

