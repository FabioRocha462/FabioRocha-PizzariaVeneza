    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        PedidoFornecedor pf1=new PedidoFornecedor();
        PedidoFornecedor fo=new PedidoFornecedor();
        double valor;
        fo.setNome(pesquisafornecedor.getText());
        try{
        valor=Double.parseDouble(valorfornecedor.getText());
        
        fo.setTotalCompras(valor);
        }catch(Exception e){
            e.getMessage();
            valorfornecedor.setText(valorfornecedor.getText().replaceAll(",", "."));
            valor=Double.parseDouble(valorfornecedor.getText());
             fo.setTotalCompras(valor);
        }
        fo.setEntrega(datafornecedor.getText());
        fo.setPedido(pedidofornecedor.getText());
        pf1.fazerpedido(fo);
        
        NotaFornecedorJanela nfj=new NotaFornecedorJanela();
        nfj.setVisible(true);
        
        nomefornecedor.setText("");
        valorfornecedor.setText("");
        datafornecedor.setText("");
        pedidofornecedor.setText("");
        pesquisafornecedor.setText("");
        NotaFornecedorJanela nf=new NotaFornecedorJanela();
        nf.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

